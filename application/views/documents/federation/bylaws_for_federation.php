    <div class="col-sm-12 col-md-12 text-center">
        <p class="font-weight-bold">Article III<br>Administration</p>
    </div>
  </div>
  <div class="row">
    <div class="col-sm-12 col-md-12 text-left">
        <p class="font-weight-regular text-justify">Section 1. <i class="font-weight-bold">The General Assembly (GA).</i> The General Assembly is composed of all the members entitled to vote, duly assembled and constituting a quorum and is the highest policy-making body of the Federation.</p>
    </div>
  </div>
  <div class="row">
    <div class="col-sm-12 col-md-12 text-left">
        <p class="font-weight-regular text-justify">Section 2. <i class="font-weight-bold">Powers of the General Assembly.</i> Subject to the pertinent provisions of the Cooperative Code and the rules issued thereunder, the General Assembly shall have the following exclusive powers which cannot be delegated:</p>
    </div>
  </div>
  <div class="row">
    <div class="col-sm-12 col-md-12">
      <ol class="text-justify" type="a">
				<li>To determine and approve amendments to the cooperative Articles of Cooperation and By-laws; </li>
				<li>To elect or appoint the members of the board of directors, and to remove them for cause; </li>
				<li>To approve developmental plans of the cooperative; and </li>
				<li> To delegate the following power/s to a smaller body of the Federation: </li>
					<ol class="text-justify" type="i">
					<li>___________________________________________ </li>
					<li>___________________________________________ </li>
					</ol>
				</ol>
    </div>
  </div>
  <div class="row">
    <div class="col-sm-12 col-md-12 text-left">
        <p class="font-weight-regular text-justify">Section 3. <i class="font-weight-bold">Meetings.</i> Meetings of the General Assembly, board of directors and committees may be regular or special. All proceedings and businesses undertaken at any meeting of the general assembly or Board of Directors, if within the powers or authority of the Federation, there being a quorum, shall be valid.</p>
    </div>
  </div>
  <div class="row">
    <div class="col-sm-12 col-md-12 text-left">
        <p class="font-weight-regular text-justify">Section 4. <i class="font-weight-bold">Regular General Assembly Meeting.</i> The General Assembly shall hold its annual regular meeting at the principal office of the Federation or at any place in the Philippines within ninety (90) days after the close of its fiscal year.</p>
    </div>
  </div>
  <div class="row">
    <div class="col-sm-12 col-md-12 text-left">
        <p class="font-weight-regular text-justify">Section 5. <i class="font-weight-bold">Special General Assembly Meeting.</i> The Board of Directors may,by a majority vote of all its affiliates, call a Special General Assembly meeting at any time to consider urgent matters requiring immediate membership decision. The Board of Directors must likewise call a Special General Assembly meeting within one (1) month from receipt of a written request from:</p>
    </div>
  </div>
  <div class="row">
    <div class="col-sm-12 col-md-12">
      <ol class="text-justify" type="a">
        <li> at least ten percent (10%) of the total number of affiliates entitled to vote,</li>
        <li> the Audit Committee, or</li>
        <li> The Federation to which the Cooperative is an affiliate, o</li>
        <li> upon Order of the Cooperative Development Authority;</li>
      </ol>
    </div>
  </div>
  <div class="row">
    <div class="col-sm-12 col-md-12 text-left">
        <p class="font-weight-regular text-justify">Section 6. <i class="font-weight-bold">Notice of Meeting.</i> All notices of meetings shall be in writing and shall include the date, time, place, and agenda thereof stated therein.</p>
    </div>
  </div>
  <div class="row">
    <div class="col-sm-12 col-md-12">
      <ol class="text-justify" type="a">
          <li> Regular General Assembly Meeting.  Notice of the annual regular General Assembly meeting shall be served by the Secretary, personally or his duly authorized representative, by registered mail, or by electronic means to all affiliates of record at its last known postal address, or by posting or publication, or through other electronic means, at least one (1) week before the said meeting.  It shall be accompanied with an agenda, minutes of meeting of the last General Assembly meeting, consolidated reports of the Board of Directors and Committees, audited financial statements, and other papers which may assist the members to intelligently participate in the proceedings.</li>
					<li>Special General Assembly Meeting.  Notice of any Special General Assembly meeting shall be served by the Secretary personally or his duly authorized representative, by registered mail, or by electronic means upon each members who are entitled to vote at his last known postal address, or by posting or publication, or through other electronic means, at least one (1) week before the said meeting.  It shall state the purpose and, except for related issues, no other business shall be considered during the meeting. </li>
					<li>Waiver of Notice.  Notice of any meeting may be waived, expressly or impliedly, by the affiliate concerned. </li>
      </ol>
    </div>
  </div>
  <div class="row">
    <div class="col-sm-12 col-md-12 text-left">
        <p class="font-weight-regular text-justify">Section 7. <i class="font-weight-bold">Order of Business.</i> As far as practicable, the order of business of a regular general assembly meeting shall be:</p>
    </div>
  </div>
  <div class="row">
    <div class="col-sm-12 col-md-12">
      <ol class="text-justify" type="a">
        <li>Call to order; </li>
        <li>Declaration/Consideration of presence of quorum; </li>
        <li>Reading, consideration and approval of the minutes of the previous meeting; </li>
        <li>Presentation and approval of the reports of the board of directors, officers, and the committees, including audited financial statements of the Federation; </li>
        <li>Unfinished business; </li>
        <li>New business;
            <ol class="text-justify" type="i">
              <li>h.1 Election of directors and committee members </li>
              <li>h.2 Approval of Development and/or annual Plan and Budget </li>
              <li>h.3 Hiring of External Auditor </li>
              <li>h.4 Other related business matters </li>
            </ol>
        </li>
        <li>Announcements; and </li>
        <li>Adjournment</li>
      </ol>
    </div>
  </div>
  <div class="row">
    <div class="col-sm-12 col-md-12 text-left">
        <p class="font-weight-regular text-justify">Section 8. <i class="font-weight-bold">Quorum for General Assembly Meeting.</i> During regular or special general assembly meeting, at least _____________ percent (_______%) of the total number of affiliates entitled to vote shall constitute a quorum. </p>
    </div>
  </div>
  <div class="row mb-4">
    <div class="col-sm-12 col-md-12 text-left">
        <p class="font-weight-regular text-justify">Section 9. <i class="font-weight-bold">Voting System.</i> Only affiliates entitled to vote shall be qualified to participate and vote in any general assembly meeting. An affiliate is entitled to one basic vote and as many incentive votes but not to exceed five (5) votes. The votes cast by the representative/delegate duly authorized shall be deemed as votes cast by the affiliates.</p>
        <p class="text-justify" style="text-indent: 50px;">The incentive vote shall be determined by the Board of Directors and approved by the general assembly.</p>
        <p class="text-justify" style="text-indent: 50px;">Election of Directors and Committee members shall be by secret ballot. Action on all matters shall be in any manner that will truly and correctly reflect the will of the membership.</p>
    </div>
  </div>
  <div class="row mb-2">
    <div class="col-sm-12 col-md-12 text-center">
        <p class="font-weight-bold">Article IV<br>Board of Directors</p>
    </div>
  </div>
  <div class="row">
    <div class="col-sm-12 col-md-12 text-left">
        <p class="font-weight-regular text-justify">Section 1. <i class="font-weight-bold">Composition of the Board of Directors (BOD).</i> The Board of Directors shall be composed of _________ ( ) members.</p>
    </div>
  </div>
  <div class="row">
    <div class="col-sm-12 col-md-12 text-left">
        <p class="font-weight-regular text-justify">Section 2. <i class="font-weight-bold">Functions and Responsibilities.</i> The Board of Directors shall have the following functions and responsibilities:</p>
    </div>
  </div>
  <div class="row">
    <div class="col-sm-12 col-md-12">
      <ol class="text-justify" type="a">
    		<li>Provide general policy direction; </li>
    		<li>Formulate the strategic development plan; </li>
    		<li>Determine and prescribe the organizational and operational structure; </li>
    		<li>Review the Annual Plan and Budget and recommend for the approval of the GA; </li>
    		<li>Establish policies and procedures for the effective operation and ensure proper implementation of such; </li>
    		<li> Evaluate the capability and qualification and recommend to the General Assembly the engagement of the services of the External Auditor; </li>
    		<li>Appoint the members of the Mediation/ Conciliation and Ethics Committees and other Officers as specified in the Code and Federation’s By-laws; </li>
    		<li>Decide election related cases involving the Election Committee and its members in accordance with the Guidelines issued by the CDA, Art. 137 of Republic Act No. 9520, Memorandum Circulars issued by the Cooperative Development Authority, Alternate Dispute Resolution Act of 2004 and its suppletory laws;</li>
    		<li> Act on the recommendation of the Ethics Committee on cases involving violations of Code of Governance and Ethical Standards in accordance with the Guidelines issued by the CDA, Art. 137 of Republic Act No. 9520, Memorandum Circulars issued by the Cooperative Development Authority,  Alternate Dispute Resolution Act of 2004 and its suppletory laws; and </li>
    		<li>Perform such other functions as may be prescribed in the By-laws or authorized by the General Assembly;</li>
    	</ol>
    </div>
  </div>
  <div class="row">
    <div class="col-sm-12 col-md-12 text-left">
        <p class="font-weight-regular text-justify">Section 3. <i class="font-weight-bold">Qualifications.</i> Any member who are entitled to vote and has the following qualifications can be elected or continue as member of the Board of Directors:</p>
    </div>
  </div>
  <div class="row">
    <div class="col-sm-12 col-md-12">
      <ol class="text-justify" type="a">
        <li>Has paid the minimum capital requirement; </li>
    		<li>Has no delinquent account with the Federation; </li>
    		<li>Have continuously patronized the Federation’s services;</li>
    		<li>An affiliate in good standing for the last two (2) years;</li>
    		<li>Completed or willingness to complete within the prescribed period the required education and training whichever is applicable; and </li>
    		<li>Other qualifications prescribed in the rules and regulations of the Authority. </li>
    	</ol>
    </div>
  </div>
  <div class="row">
    <div class="col-sm-12 col-md-12 text-left">
        <p class="font-weight-regular text-justify">Section 4. <i class="font-weight-bold">Disqualifications.</i> Any affiliate representative under any of the following circumstances shall be disqualified to be elected as a member of the Board of Directors or any committee, or to continue as such:</p>
    </div>
  </div>
  <div class="row">
    <div class="col-sm-12 col-md-12">
      <ol class="text-justify" type="a">
        <li>Holding any elective position in the government, except that of a party list representative  being an officer of a cooperative he or she represents; </li>
    		<li>The members of the Board of Directors holding other position directly involved in the day-to-day operation and management of the cooperative he/she represents; </li>
    		<li>Having direct or indirect personal interest with the business of the Federation; </li>
    		<li>Having been absent for three (3) consecutive meetings or in more than fifty percent (50%) of all meetings within the twelve (12) month period unless with valid  excuse as approved by the board of directors; </li>
        <li>Being an official or employee of the Cooperative Development Authority, except in a Federation organized among themselves;</li>
        <li>Having been convicted in administrative proceedings or civil/criminal suits involving financial and/or property accountability; and </li>
    		<li>Having been disqualified by law.</li>
    	</ol>
    </div>
  </div>
  <div class="row">
    <div class="col-sm-12 col-md-12 text-left">
        <p class="font-weight-regular text-justify">Section 5. <i class="font-weight-bold">Election of Directors.</i> The members of the Board of Directors shall be elected, by secret ballot, by the affiliates, who are duly authorized representatives, who are entitled to vote during the annual regular general assembly meeting or special general assembly meeting called for the purpose. Unless earlier removed for cause, or have resigned or become incapacitated, they shall hold office for a term of _______ ( ) years or until their successors shall have been elected and qualified; Provided, that majority of the elected directors obtaining the highest number of votes during the first election after registration shall serve for two (2) years, and the remaining directors for one (1) year. Thereafter, all directors shall serve for a term of __________ ( ) years; The term of the cooperating directors shall expire upon the election of their successors in the first regular general assembly after registration.</p>
    </div>
  </div>
  <div class="row">
    <div class="col-sm-12 col-md-12 text-left">
        <p class="font-weight-regular text-justify">Section 6. <i class="font-weight-bold">Election of Officer within the Board of Directors.</i> The Board of Directors shall convene within ten (10) days after the General Assembly meeting to elect by secret ballot from among themselves the Chairperson and the Vice-Chairperson, appoint the Secretary and to elect Treasurer from outside of the Board.</p>
        <p class="text-justify" style="text-indent: 50px;">For committees elected by the General Assembly and/or appointed by the BOD, procedural process of electing the Chairperson, Vice-Chairperson or other positions among themselves should be in accordance with the process mentioned above.</p>
    </div>
  </div>
  <div class="row">
    <div class="col-sm-12 col-md-12 text-left">
        <p class="font-weight-regular text-justify">Section 7. <i class="font-weight-bold">Meeting of the Board of Directors.</i> The regular meeting of the Board of Directors shall be held at least once a month. However, the Chairperson or majority of the directors may at any time call a special Board meeting to consider urgent matters. The call shall be addressed and delivered through the Secretary stating the date, time and place of such meeting and the matters to be considered. Notice of regular and special meetings of the Board of Directors, unless dispensed with, shall be served by the Secretary in writing or thru electronic means to each director at least two (2) days before such meeting.</p>
        <p class="text-justify" style="text-indent: 50px;">Majority of the total number of directors constitutes a quorum to transact business of the meeting. Any decision or action taken by the majority members of the Board of Directors in a meeting duly assembled shall be a valid cooperative act.</p>
    </div>
  </div>
  <div class="row">
    <div class="col-sm-12 col-md-12 text-left">
        <p class="font-weight-regular text-justify">Section 8. <i class="font-weight-bold">Vacancies.</i> Any vacancy occurring in the Board of Directors by reason of death, incapacity, removal or resignation may be filled-up by a majority vote of the remaining directors, if still constituting a quorum; otherwise, such vacancy shall be filled by the General Assembly in a regular or special meeting called for the purpose. The elected director shall serve only for the unexpired term of his predecessor in office.</p>
        <p class="text-justify" style="text-indent: 50px;">In the event that the General Assembly failed to muster a quorum to fill the positions vacated by directors whose term have expired and said directors refuse to continue their functions on a hold-over capacity, the remaining members of the Board together with the members of the Audit Committee shall designate, from the qualified regular members of the General Assembly, their replacements who shall serve temporarily as such until their successors shall have been elected and qualified in a regular or special General Assembly meeting called for the purpose.</p>
        <p class="text-justify" style="text-indent: 50px;">If a vacancy occurs in any elective committee it shall be filled by the remaining members of the said committee, if still constituting a quorum, otherwise, the Board, in its discretion, may appoint or hold a special election to fill such vacancy.</p>
    </div>
  </div>
  <div class="row">
    <div class="col-sm-12 col-md-12 text-left">
        <p class="font-weight-regular text-justify">Section 9. <i class="font-weight-bold">Removal of Members of the Board of Directors and Committee Members.</i> All complaints for the removal of any elected officer shall be filed with the Board of Directors and such officer shall be given the opportunity to be heard. Majority of the Board of Directors may place the officer concerned under preventive suspension pending the resolution of the investigation. Upon finding of a prima facie evidence of guilt, the Board of Directors shall present its recommendation for removal to the General Assembly. An elective officer may be removed by three-fourths (¾) of the regular members present and constituting a quorum, in a regular or special general assembly meeting called for the purpose. The officer concerned shall be given the opportunity to be heard at said Assembly. For this purpose, the Board of Directors shall provide policy on suspension.</p>
        <p class="text-justify" style="text-indent: 50px;">In cases where the officers sought to be removed consist of the majority of the BOD, at least 10% of the affiliates with voting rights may file a petition with the CDA, upon failure of the BOD to call an assembly meeting for the purpose to commence the proceeding for their removal within thirty (30) days from notice. The decision of the GA on the matter is final and executory.</p>
        <p class="text-justify" style="text-indent: 50px;">An officer appointed by the Board of Directors may be removed from office for cause by a majority vote of all the members of the Board of Directors.</p>
    </div>
