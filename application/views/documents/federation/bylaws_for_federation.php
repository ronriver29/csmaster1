  <div class="row mb-2">
    <div class="col-sm-12 col-md-12 text-center">
        <p class="font-weight-bold">Article VII<br>Capital Structure</p>
    </div>
  </div>
  <div class="row">
    <div class="col-sm-12 col-md-12 text-left">
        <p class="font-weight-regular text-justify">Section 1. <i class="font-weight-bold">Source of Funds.</i> The Federation may derive its funds from any or all of the following sources:</p>
    </div>
  </div>
  <div class="row">
    <div class="col-sm-12 col-md-12">
      <ol class="text-justify" type="a">
        <li>Affiliates’ share capital contribution; </li>
  			<li> Loans and borrowings including deposits; </li>
  			<li>Revolving capital build-up which consist of the deferred payment of patronage refund or interest on share capital; </li>
  			<li> Subsidies, grants, legacies, aids, donation and such other assistance from any local or foreign institution, public or private; </li>
  			<li>Retentions from the proceeds of services acquired /goods procured by members; and </li>
  			<li>Other sources of funds as may be authorized by law. </li>
  		</ol>
    </div>
  </div>
  <div class="row">
    <div class="col-sm-12 col-md-12 text-left">
        <p class="font-weight-regular text-justify">Section 2. <i class="font-weight-bold">Continuous Capital Build-Up.</i> Every member shall have invested in any or all of the following:</p>
    </div>
  </div>
  <div class="row">
    <div class="col-sm-12 col-md-12">
      <ol class="text-justify" type="a">
        <li>At least _____________Pesos (P_________)per annum; </li>
    		<li>At least _____ percent (__%) of affiliate’s annual interest on capital and patronage refund; and </li>
    		<li>At least _________ percent (__%) of each good procured /service acquired from the Federation. </li>
  		</ol>
    </div>
  </div>
  <div class="row">
    <div class="col-sm-12 col-md-12 text-left">
        <p class="font-weight-regular text-justify">Section 3. <i class="font-weight-bold">Borrowing.</i> The Board of Directors, upon approval of the General Assembly, may borrow funds from any source, local or foreign, under such terms and conditions that best serve the interest of the Federation</p>
    </div>
  </div>
  <div class="row">
    <div class="col-sm-12 col-md-12 text-left">
        <p class="font-weight-regular text-justify">Section 4. <i class="font-weight-bold">Revolving Capital.</i> To strengthen the capital structure of the Federation, the general assembly may authorize the Board of Directors to raise a revolving capital by deferring the payment of patronage refunds and interest on share capital, or such other schemes as may be legally adopted. To implement this provision, the Board of Directors shall issue a Revolving Capital Certificate with serial number, name, rate of interest, date of retirement and such other privileges or restrictions as may be deemed just and equitable.</p>
    </div>
  </div>
  <div class="row">
    <div class="col-sm-12 col-md-12 text-left">
        <p class="font-weight-regular text-justify">Section 5. <i class="font-weight-bold">Retentions.</i> The general assembly may authorize the Board of Directors to raise additional capital by deducting a certain percent on a per unit basis from the proceeds of services acquired and/or goods procured by members.</p>
    </div>
  </div>
  <div class="row">
    <div class="col-sm-12 col-md-12 text-left">
        <p class="font-weight-regular text-justify">Section 6. <i class="font-weight-bold">Share Capital Contribution.</i> Share Capital Contribution refers to the value of capital subscribed and paid for by an affiliate in accordance with its Articles of Cooperation.</p>
    </div>
  </div>
  <div class="row">
    <div class="col-sm-12 col-md-12 text-left">
        <p class="font-weight-regular text-justify">Section 7. <i class="font-weight-bold">Fines on Unpaid Subscribed Share Capital.</i> The Board of Directors shall prescribe a reasonable fine for unpaid subscription of share capital.</p>
    </div>
  </div>
  <div class="row">
    <div class="col-sm-12 col-md-12 text-left">
        <p class="font-weight-regular text-justify">Section 7. <i class="font-weight-bold">Share Capital Certificate.</i> The Board of directors shall issue a Share Capital Certificate only to an affiliate who has fully paid his subscription. The Certificate shall be serially numbered and contain the share holder’s name, the number of shares owned, the par value, and duly signed by the Chairperson and the Secretary, and bearing the official seal of the Federation. All certificates issued and/or transferred shall be registered in the Federation’s Share and Transfer Book.</p>
        <p class="text-justify" style="text-indent: 50px;">The number of paid share required for the issuance of Share Capital Certificate shall be determined by the Board of Directors.</p>
        <p class="text-justify" style="text-indent: 50px;">The shares may be purchased, owned or held only by the <u>primary</u> cooperative who are eligible for membership. Subject to existing government rules or laws, interests shall be paid only to paid-up shares which may be in cash; or credited as payment of unpaid subscriptions, outstanding accounts, or additional shares or to the revolving fund of the Federation.</p>
    </div>
  </div>
  <div class="row">
    <div class="col-sm-12 col-md-12 text-left">
        <p class="font-weight-regular text-justify">Section 8. <i class="font-weight-bold">Transfer of Shares.</i> The Federation shall have the first option to buy any share offered for sale. The amount to be paid for such shares shall be the par value provided that: </p>
    </div>
  </div>
  <div class="row">
    <div class="col-sm-12 col-md-12">
      <ol class="text-justify" type="a">
        <li> The Affiliate has held such shares or interests for not less than one (1) year; </li>
    		<li> The transfer is made to an affiliate of the Federation or eligible cooperative that falls within the field of membership of the Federation; and </li>
    		<li>The Board has approved such transfer. </li>
    	</ol>
      <p class="text-justify" style="text-indent: 50px;">The transfer of shares shall not be binding to the Federation until such transfer has been registered in the share and transfer book. No transfer shall be completed until the old certificate have been endorsed and surrendered to the Federation and a new certificate is issued in the name of the affiliate. The corresponding transfer fee shall be collected from the transferee as prescribed in the federation policy.</p>
      <p class="text-justify" style="text-indent: 50px;">In case of lost or destroyed share certificate, the Board of Directors may issue a replacement after the affiliate-owner thereof executes a sworn affidavit, setting forth the following:</p>
    </div>
  </div>
  <div class="row mb-4">
    <div class="col-sm-12 col-md-12">
      <ol class="text-justify" type="a">
        <li>Circumstances as to how, when and where said certificate was lost or destroyed; </li>
    		<li> The serial number of the certificate; and the number of shares it represents; </li>
    		<li> The lost or destroyed certificate has never been transferred, sold or endorsed to any third party, that should the same be found, the owner shall surrender it to the Federation; and </li>
    		<li>That any false representation or statement made in the aforesaid affidavit shall be a ground for expulsion from the Federation. </li>
    	</ol>
    </div>
  </div>
  <div class="row mb-2">
    <div class="col-sm-12 col-md-12 text-center">
        <p class="font-weight-bold">Article VIII<br>Operations</p>
    </div>
  </div>
  <div class="row">
    <div class="col-sm-12 col-md-12 text-left">
        <p class="font-weight-regular text-justify">Section 1. <i class="font-weight-bold">Primary Consideration.</i> Adhering to the principle of service, the Federation shall endeavor to:</p>
    </div>
  </div>
  <div class="row mb-4">
    <div class="col-sm-12 col-md-12">
      <ol class="text-justify" type="a">
        <li> Engage in:
    			<ol type="i">
    				<li>____________________________________________________ </li>
    				<li>____________________________________________________ </li>
    				<li>____________________________________________________ </li>
    			</ol>
        </li>
        <li> Formulate and implement program strategies that will provide its affiliates and the communities within its area of operation needed goods/services; </li>
    		<li> Adopt and implement plans and programs which insures the continued build-up of the Federations’ capital structure with the end view of establishing other needed services for the members and the public; </li>
    		<li> Formulate and implement studies and/or programs that will address the needs of affiliate; and </li>
    		<li> Collect CETF and other dues from its affiliates. </li>
    	</ol>
    </div>
  </div>
  <div class="row mb-2">
    <div class="col-sm-12 col-md-12 text-center">
        <p class="font-weight-bold">Article IX<br>Allocation and Distribution of Net Surplus</p>
    </div>
  </div>
  <div class="row">
    <div class="col-sm-12 col-md-12 text-left">
        <p class="font-weight-regular text-justify">Section 1. <i class="font-weight-bold">Allocation.</i> At the end of its fiscal year, the Federation shall allocate and distribute its net surplus as follows:</p>
    </div>
  </div>
  <div class="row">
    <div class="col-sm-12 col-md-12">
      <ol class="text-justify" type="a">
        <li> <i class="font-weight-bold">Reserve Fund.</i> ________ percent (____%)shall be set aside for Reserve Fund subject to the following rules, provided, that in the first five (5) years of operation after registration, this amount shall not be less than fifty per centum (50%) of the net surplus:
    			<ol type="i">
            <li>The reserve fund shall be used for the stability of the Federation and to meet net losses in its operations. The general assembly may decrease the amount allocated to the reserve fund when it has already exceeded the authorized share capital. Any sum recovered on items previously charged to the reserve fund shall be credited to such fund. </li>
  					<li> The reserve fund shall not be utilized for investment, other than those allowed in the Republic Act No. 9520.  Such sum of the reserve fund in excess of the authorized share capital may be used at any time for any project that would expand the operations of the Federation upon the resolution of the general assembly. </li>
  					<li> Upon the dissolution of the Federation, the reserve fund shall not be distributed among the affiliates. However, the general assembly may resolve:
              <ol type="a">
  							<li> To establish usufructuary fund for the benefit of any federation or union to which the Federation is affiliated; or </li>
  							<li>To donate, contribute or otherwise dispose of the amount for the benefit of the community where the Federation operates.  If the affiliate could not decide on the disposition of the reserve fund, the same shall be given to the federation or union to which the cooperative is affiliated. </li>
  						</ol>
            </li>
    			</ol>
        </li>
        <li><i class="font-weight-bold">Education and Training Fund.</i> ________ percent (____%)shall be set aside for Education and Training Fund.
          <ol type="i">
  					<li>Half of the amount allocated to the education and training fund annually under this subsection may be spent by the Federation for education and training purposes; while the other half may be remitted to a union or federation chosen by the Federation or of which it is a affiliated with. </li>
  					<li>Upon the dissolution of the Federation, the unexpended balance of the education and training fund pertaining to the Federation shall be credited to the Federations’ education and training fund of the chosen union or federation. </li>
  				</ol>
        </li>
        <li><i class="font-weight-bold">Community Development Fund.</i>  ________ percent (____%)shall be used for projects and activities that will benefit the community where the Federation operates. </li>
        <li><i class="font-weight-bold">Optional Fund.</i> ________ percent (____%)shall be set aside for Optional Fund for land and building, and any other necessary fund. </li>
    	</ol>
    </div>
  </div>
  <div class="row">
    <div class="col-sm-12 col-md-12 text-left">
        <p class="font-weight-regular text-justify">Section 2. <i class="font-weight-bold">Interest on Share Capital and Patronage Refund.</i> The remaining net surplus shall be made available to the affiliates in the form of interest on share capital not to exceed the normal rate of return on investment and patronage refunds. Provided, that any amount remaining after the allowable interest and the patronage refund have been deducted shall be credited to the reserved to the reserve fund. The sum allocated for patronage refund shall be made available at the same rate to all patrons of the Federation in proportion to their individual patronage, provided that:</p>
    </div>
  </div>
  <div class="row mb-4">
    <div class="col-sm-12 col-md-12">
      <ol class="text-justify" type="a">
        <li>In the case of a affiliate/member patron with paid-up share capital contribution, his/her proportionate amount of  patronage refund shall be paid to him unless he/she agrees to credit the amount to his/her  account as additional share capital contribution; </li>
    		<li> In the case of affiliate/member patron with unpaid share capital contribution, its/his/her proportionate amount of  patronage refund shall be credited to its/his/her account until the share capital has been fully paid;</li>
    		<li>In the case of non-affiliate/member patron, its/his/her proportionate amount of  patronage refunds shall be set aside in a general fund for such patron and shall be allocated to individual non-member patron and only upon request and presentation of evidence of the amount of his/her patronage. The amount so allocated shall be credited to such patron toward payment of the minimum capital contribution for membership. When a sum equal to this amount has accumulated at any time within two (2 ) years, such patron shall be deemed and become an affiliate of the Federation if he so agrees or requests and complies with the provisions of the bylaws for admission to membership; and </li>
    		<li> If within the period specified hereof, any subscriber  who has not fully paid his subscribed share capital or any non-affiliate patron who has accumulated, the sum necessary for membership, but who does not request nor agree to become an affiliate or fails to comply with the provisions of this bylaws for admission to membership,  the amount so accumulated or credited to their account together with any part of the general fund for non-member patron shall be credited to the reserve fund or to the education and training fund, at the option of the Federation.</li>
    	</ol>
    </div>
  </div>
  <div class="row mb-2">
    <div class="col-sm-12 col-md-12 text-center">
        <p class="font-weight-bold">Article X<br>Settlement of Disputes</p>
    </div>
  </div>
  <div class="row">
    <div class="col-sm-12 col-md-12 text-left">
        <p class="font-weight-regular text-justify">Section 1. <i class="font-weight-bold">Mediation and Conciliation.</i> All inter and intra-federation disputes shall be settled within the Federation in accordance with the Guidelines issued by the Cooperative Development Authority, Art. 137 0f Republic Act No. 9520 and its Implementing Rules and Regulations, Alternative Dispute Resolution Act of 2004 and its suppletory laws.</p>
    </div>
  </div>
  <div class="row mb-4">
    <div class="col-sm-12 col-md-12 text-left">
        <p class="font-weight-regular text-justify">Section 2. <i class="font-weight-bold">Voluntary Arbitration.</i> Any dispute, controversy or claim arising out of or relating to this By-laws, the cooperative law and related rules, administrative guidelines of the Cooperative Development Authority, including disputes involving members, officers, directors, and committee members, intra-federation disputes and related issues, and any question regarding the existence, interpretation, validity, breach or termination of agreements, or the membership/general assembly concerns shall be exclusively referred to and finally resolved by voluntary arbitration under the institutional rules promulgated by the Cooperative Development Authority, after compliance with the conciliation or mediation mechanisms embodied in this By-laws, and in such other applicable laws.</p>
    </div>
  </div>
  <div class="row mb-2">
    <div class="col-sm-12 col-md-12 text-center">
        <p class="font-weight-bold">Article XI<br>Miscellaneous</p>
    </div>
  </div>
  <div class="row">
    <div class="col-sm-12 col-md-12 text-left">
        <p class="font-weight-regular text-justify">Section 1. <i class="font-weight-bold">Investment of Capital.</i>The Federation may invest its capital in any or all of the following:</p>
    </div>
  </div>
  <div class="row">
    <div class="col-sm-12 col-md-12">
      <ol class="text-justify" type="a">
        <li> Shares or debentures or securities; </li>
    		<li>Any reputable bank in the locality or any tertiary federation of which it is a member and cooperative banks; </li>
    		<li>Securities issued or guaranteed by Government; </li>
    		<li> Real Estate primarily for the use of the Federation or its affiliates; or </li>
    		<li> In any other manner approved by the general assembly. </li>
    	</ol>
    </div>
  </div>
  <div class="row">
    <div class="col-sm-12 col-md-12 text-left">
        <p class="font-weight-regular text-justify">Section 2. <i class="font-weight-bold">Accounting System.</i> The Federation shall keep, maintain and preserve all its books of accounts and other financial records in accordance with generally accepted accounting principles and practices, applied consistently from year to year, and subject to existing laws, rules and regulations.</p>
    </div>
  </div>
  <div class="row">
    <div class="col-sm-12 col-md-12 text-left">
        <p class="font-weight-regular text-justify">Section 3. <i class="font-weight-bold">Financial Audit, Performance Audit, and Social Audit.</i> At least once a year, the Board of Directors shall cause, in consultation with the Audit Committee, the audit of the books of accounts of the Federation, performance audit and social audit by CDA Accredited Independent Certified Public Accountant, Accredited Social Auditor, and Federation Compliance Officer/Audit Committee.</p>
    </div>
  </div>
  <div class="row">
    <div class="col-sm-12 col-md-12 text-left">
        <p class="font-weight-regular text-justify">Section 4. <i class="font-weight-bold">Annual Report.</i> During the annual regular assembly meeting, the Federation shall submit a report of its operation to the General Assembly together with the Audited Financial Statements, Performance Audit and Social Audit reports. The Cooperative Annual Performance Report shall be certified by the Chairperson and Manager of the Federation as true and correct in all aspects to the best of their knowledge. The Audited Financial Statements and Social Audit Reports shall be certified by CDA Accredited Independent Auditors. </p>
        <p class="text-justify" style="text-indent: 50px;">The Federation shall submit the following reports to the Authority within (120) days from the end of every calendar year:</p>
    </div>
  </div>
  <div class="row mb-4">
    <div class="col-sm-12 col-md-12">
      <ol class="text-justify" type="a">
        <li>Cooperative Annual Performance Report (CAPR); </li>
    		<li> Social Audit Report; </li>
    		<li> Performance Report; </li>
    		<li>Audited Financial Statement duly stamped “Received” by BIR; </li>
    		<li>List of officers and trainings undertaken/completed; </li>
    		<li>List of cooperatives which have remitted their respective Cooperative Education and Training Funds (CETF); </li>
    		<li>Consultancy assistance to include the nature and cost; and </li>
    		<li>Other training activities undertaken specifying therein the nature, participants, and cost of the activity. </li>
    	</ol>
    </div>
  </div>
  <div class="row mb-2">
    <div class="col-sm-12 col-md-12 text-center">
        <p class="font-weight-bold">Article XI<br>Ammendments</p>
    </div>
  </div>
  <div class="row mb-4">
    <div class="col-sm-12 col-md-12 text-left">
        <p class="font-weight-regular text-justify">Section 1. <i class="font-weight-bold">Amendment of Articles of Cooperation and By-laws.</i> Amendments to the Articles of Cooperation and this By-Laws may be adopted by at least two-thirds (2/3) votes of all members with voting rights without prejudice to the rights of dissenting members to withdraw their membership under the provisions of the Philippine Cooperative Code of 2008.</p>
        <p class="text-justify mb-4" style="text-indent: 50px;">The amendment/s shall take effect upon approval by the Cooperative Development Authority.</p>
