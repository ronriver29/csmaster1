  			<li>Avail the services of the Federation, subject to certain conditions as may be prescribed by the Board of Directors;</li>
  			<li>Inspect and examine the books of accounts, the minutes books, the share register, and other records of the Federation during reasonable office hours;</li>
  			<li>Secure copies of Federation’s records/documents pertaining to the account information of the concerned affiliate; and</li>
  			<li>Such other rights and privileges as may be granted by the General Assembly.</li>
  		</ol>
