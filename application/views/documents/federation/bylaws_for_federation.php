    <div class="col-sm-12 col-md-12 text-left">
        <p class="font-weight-regular text-justify">Section 13. <i class="font-weight-bold">Manner of Involuntary Termination.</i> The Board of Directors shall notify in writing the affiliate which is being considered for termination and shall give them the opportunity to be heard.</p>
        <p class="text-justify" style="text-indent: 50px;">The decision of the Board of Directors shall be in writing and shall be communicated in person or by registered mail to said member and shall be appealable within thirty (30) days from receipt thereof to the General Assembly whose decision shall be final.</p>
    </div>
  </div>
  <div class="row mb-4">
    <div class="col-sm-12 col-md-12 text-left">
        <p class="font-weight-regular text-justify">Section 14. <i class="font-weight-bold">Refund of Share Capital Contribution.</i> An affiliate member whose membership is terminated shall be entitled to a refund of its share capital contribution and all other interests in the federation. However, such refund shall not be made if upon payment the value of the assets of the Federation would be less than the aggregate amount of its debts and liabilities exclusive of its share capital contribution. In which case, the terminated affiliate shall continue to be entitled to the interest of its share capital contributions, patronage refund and the use of the services of the Federation until such time that all its interests in the Federation shall have been duly paid.</p>
    </div>
  </div>
