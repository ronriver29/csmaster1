    <div class="col-sm-12 col-md-12 text-left">
        <p class="font-weight-regular text-justify">Section 9. <i class="font-weight-bold">Affiliate Entitled to Vote.</i> Any regular member who meets the following conditions is a member entitled to vote:</p>
    </div>
  </div>
  <div class="row">
    <div class="col-sm-12 col-md-12">
      <ol class="text-justify" type="a">
